package Pages;

public class Calculater {

    public float AddProcess(float x, float y) {
        return x + y;
    }


    public float SubtractProcess(float x, float y) {
        return x - y;
    }

    public float MultiplyProcess(float x, float y) {
        return x * y;
    }


    public float DivideProcess(float x, float y) {
        return (x / y);
    }


}
